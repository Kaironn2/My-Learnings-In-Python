# Conversão de tipos pode ser chamada de:
# Type convertion, typecasting, coercion e coerção.
# Esse é o ato de converter um tipo em outro 
# Tipos imutáveis e primitivos: 
# str, int, float, bool

# Um mesmo operador pode ter mais de uma função, isso é chamado de polimorfia na programação

print(1 + 1) # somar
print('a' + 'b') # concatenar

print(int('1'), type(int('1')))
print(float('1') + 1)
print(str('11') + 'b')
print(bool(' '))
