# Operadores de comparação (relacionais)
# OP -------- SIGNIFICADO ------------ Exemplo (True)
# > --------- Maior ------------------ 2 > 1 
# < --------- Menor ------------------ 1 < 2
# >= -------- Maior ou igual --------- 3 >= 2
# <= -------- Menor ou igual --------- 2 <= 3
# == -------- Igual ------------------ 'a' == 'b'
# != -------- Diferente -------------- 'a' != 'b'

# Toda vez que você fazer uma operação de comparação, 
# ela retornará um valor bool, ou seja, true ou false.
# ex: 2 == 2 retorna True
