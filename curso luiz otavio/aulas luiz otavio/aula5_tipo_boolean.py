# Tipo de dado bool (boolean)
# Ao questionar um programa, só existem duas respostas possíveis:
# sim(True) ou não(False)
# Existem operadores para "questionar", dentre eles, o ==
# é um operador lógico que questiona se um valor é igual a outro

print(10 == 10) # Sim -> True (verdadeiro)
print(10 == 11) # Não -> False (falso)
