# Ambientes virtuais em Python (venv)
# Um ambiente virtual carrega toda a sua instalação
# do Python para uma pasta no caminho escolhido.
# Ao ativar um ambiente virtual, a instalação do
# ambiente virtual será usada.
# venv é o módulo que vamos usar para
# criar ambientes virtuais.
# Você pode dar o nome que preferir para um
# ambiente virtual, mas os mais comuns são:
# venv env .venv .env

# Para criar um ambiente virtual no windows, no terminal insira a linha de código:
# python -m venv nomedoambiente
# Geralmente, usamos "venv" ou "env" como nome do ambiente virtual

# para ver o path do python, no powershell use:
# gcm python ou gcm python -Syntax

# Para ativar o ambiente virtual, digite o caminho do activate
# venv/Scripts/activate
# E para desativar o ambiente virtual, digite deactivate

# Para instalar pacotes:
# pip install nomedopacote
# Para desinstalar pacotes:
# pip install nomedopacote
# Para listar versões do pacote:
# pip index versions pymysql
